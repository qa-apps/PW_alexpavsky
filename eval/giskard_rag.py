def query_rag(question: str) -> str:
    """Call the production RAG API and return the answer string."""
    try:
        r = requests.post(
            f"{RAG_API}/api/rag/query",
            json={"query": question},
            timeout=60,
        )
        r.raise_for_status()
        return r.json().get("answer", "")
    except Exception as e:
        log(f"    RAG call failed: {e}")
        return ""


def main() -> None:
    log("=" * 72)
    log("Giskard RAG Evaluation")
    log("=" * 72)
    log(f"  RAG_API:       {RAG_API}")
    log(f"  num_questions: {NUM_QUESTIONS}")
    log("")

    # Lazy imports so a missing dep gives a clear error.
    try:
        import pandas as pd
        from giskard.rag import generate_testset, evaluate, KnowledgeBase
    except ImportError as e:
        fail(f"Missing dependency: {e}. Run: pip install -r eval/requirements.txt")

    # Use the same rotating judge as Ragas — Giskard needs an OpenAI-compatible
    # client for both question generation and answer evaluation.
    from rotating_llm import build_provider_list
    providers = build_provider_list()
    if not providers:
        fail("No LLM provider keys configured — set GROQ_API_KEY (and others).")

    primary = providers[0]
    log(f"  Judge: {primary['name']}/{primary['model']} (+{len(providers)-1} fallbacks)")

    # Point Giskard's LLM client at our primary provider. Giskard reads the
    # OPENAI_API_KEY / OPENAI_API_BASE convention internally for its judge.
    os.environ["OPENAI_API_KEY"] = primary["api_key"]
    os.environ["OPENAI_API_BASE"] = primary["base_url"]
    try:
        import giskard
        # LiteLLM-style: "openai/<model>" routes via OPENAI_API_BASE we just set.
        giskard.llm.set_llm_model(f"openai/{primary['model']}")
        # Groq/Cerebras/etc don't serve embeddings — use HuggingFace router
        # (free, OpenAI-compatible). Requires HF_TOKEN.
        if os.environ.get("HF_TOKEN"):
            giskard.llm.set_embedding_model(
                "huggingface/sentence-transformers/all-MiniLM-L6-v2"
            )
    except Exception as e:
        log(f"  WARN: Giskard config helper failed ({e}); relying on env defaults")

    log("")
    log("Step 1/3: Building knowledge base...")
    df = pd.DataFrame({"text": KB_CHUNKS})
    knowledge_base = KnowledgeBase(df)
    log(f"  KB chunks: {len(df)}")

    log("")
    log(f"Step 2/3: Auto-generating {NUM_QUESTIONS} questions...")
    t0 = time.time()
    try:
        testset = generate_testset(
            knowledge_base,
            num_questions=NUM_QUESTIONS,
            language="en",
            agent_description=(
                "QA assistant for alexpavsky.com — answers questions about "
                "Playwright, LLM evaluation, RAG testing, MCP, agentic testing, "
                "cloud QA, and the FinNova case study."
            ),
        )
    except Exception as e:
        fail(f"Test generation failed: {e}")
    log(f"  Generated {len(testset)} questions in {time.time()-t0:.1f}s")

    log("")
    log("Step 3/3: Running RAG against generated questions + evaluating...")

    def rag_callable(question: str, history=None):
        return query_rag(question)

    t0 = time.time()
    try:
        report = evaluate(
            rag_callable,
            testset=testset,
            knowledge_base=knowledge_base,
        )
    except Exception as e:
        fail(f"Evaluation failed: {e}")
    log(f"  Done in {time.time()-t0:.1f}s")

    # Persist report.
    html_path = RESULTS_DIR / "giskard_rag.html"
    json_path = RESULTS_DIR / "giskard_rag.json"
    try:
        report.to_html(str(html_path))
        log(f"  HTML report: {html_path}")
    except Exception as e:
        log(f"  WARN: could not write HTML: {e}")

    summary = {
        "rag_api": RAG_API,
        "num_questions": NUM_QUESTIONS,
        "judge": f"{primary['name']}/{primary['model']}",
        "correctness": None,
    }
    try:
        # Giskard's report exposes a correctness metric on the full set.
        correctness = float(report.correctness) if hasattr(report, "correctness") else None
        summary["correctness"] = correctness
    except Exception:
        pass

    json_path.write_text(json.dumps(summary, indent=2))
    log(f"  JSON summary: {json_path}")

    log("")
    log("=" * 72)
    log("Giskard RAG eval complete.")
    log(f"  Correctness: {summary['correctness']}")
    log("=" * 72)


if __name__ == "__main__":
    main()
